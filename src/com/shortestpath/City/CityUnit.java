package com.shortestpath.City;

public class CityUnit {
    public int cityID;
    public String cityName;
    public double xcoordinate;
    public double ycoordinate;

    public CityUnit(int id,String name,double x,double y) {
        this.cityID=id;
        this.cityName=name;
        this.xcoordinate=x;
        this.ycoordinate=y;
    }

}
